md"""
# 4.8 Varianz und Standardabweichung

Um aus einer gegebenen Stichprobe $x_1, \dots, x_N$ mit $N \ge 2$ Schätzwerte für den Mittelwert und die Varianz zu erhalten, nutzt man das arithmetische Mittel

$$a_N = \frac{1}{N} \sum_{i=1}^N x_i$$

und die empirische Varianz

$$b_N^2 = \frac{1}{N - 1} \sum_{i=1}^N (x_i - a_N)^2.$$

Wenn man die Formeln direkt verwendet, muss man zwei mal über die Daten iterieren: erst berechnet man $\mu$, danach $\sigma^2$. Wir können die empirische Varianz aber auch schreiben als

$$\begin{equation*}\begin{aligned}
b_N^2 
&= \frac{1}{N - 1} \sum_{i=1}^N (x_i - a_N)^2
\\&= \frac{1}{N - 1} \sum_{i=1}^N (x_i^2 - 2 a_N x_i + a_N^2)
\\&= \frac{1}{N - 1} \sum_{i=1}^N x_i^2 - \frac{2 a_N}{N - 1} \sum_{i=1}^N x_i + \frac{N}{N - 1} a_N^2
\\&= \frac{1}{N - 1} \sum_{i=1}^N x_i^2 - \frac{2 N}{N - 1} a_N^2 + \frac{N}{N - 1} a_N^2
\\&= \frac{1}{N - 1} \sum_{i=1}^N x_i^2 - \frac{N}{N - 1} a_N^2.
\end{aligned}\end{equation*}$$

Hierfür reicht eine Iteration über alle Daten aus. Welche Variante ist zu bevorzugen?
"""

# ╔═╡ dee6ab34-d996-43bf-ae42-e8a913c6a319
function two_pass_stats(x)
	mean(x), var(x)
end

# ╔═╡ 9dcc59f7-d83f-4989-bf82-eccf07704d9d
function naive_stats(x)
	sum_x = zero(eltype(x))
	sum_x2 = zero(eltype(x))
	
	for xi in x
		sum_x += xi
		sum_x2 += xi^2
	end
	
	n = length(x)
	mean_x = sum_x / n
	var_x = sum_x2 / (n - 1) - n / (n - 1) * mean_x^2
	
	return mean_x, var_x
end

# ╔═╡ a7320b8b-7698-43a4-9582-c565ecd6c5f6
function online_stats(x)
	s = Series(Mean(), Variance())
	for xi in x
	   fit!(s, xi)
	end
	return value(s)
end

